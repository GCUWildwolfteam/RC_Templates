/**
  *@brief:求和校验 所有字节之和为0
  */
uint16_t Checksum_Sum(uint8_t* buf,uint16_t len)
{	
	uint8_t check = 0x00;

	while(len--)
	{
		check += *buf++;
	}
	check = 256 - check%256;

	return (check)&0x00ff;
}

/**
  *@brief:异或校验 所有字节异或
  */
uint16_t Checksum_XOR(uint8_t* buf, uint16_t len)
{
	uint8_t check = 0;
	uint8_t checkTemp = 0;

	check = *buf;
	buf++;
	checkTemp = *buf;
	buf++;
	check = check^checkTemp;
	
	len -=2;
	while(len--)
	{
		check = check^(*buf++);
	}
	
	return (check)&0x00ff;
}

/**
  *@brief:CRC8 校验
  */
uint16_t Checksum_CRC8(uint8_t *buf,uint16_t len)
{	
	uint8_t check = 0;

	while(len--)
	{
		check = CRC8Table[check^(*buf++)];
	}

	return (check)&0x00ff;	
}

/**
  *@brief:CRC16 校验,高字节在前，低字节在后
  */
uint16_t Checksum_CRC16(uint8_t *buf,uint16_t len)
{
	int index;
	uint16_t check = 0;
	uint8_t crc_low=0xff;
	uint8_t crc_high=0xff;

	while(len--)
	{
		index = crc_high^(*buf++);
		crc_high = crc_low^CRC16HiTable[index];
		crc_low = CRC16LoTable[index];
	}
	check +=crc_high;
	check <<=8;
	check +=crc_low;

	return check;
}


